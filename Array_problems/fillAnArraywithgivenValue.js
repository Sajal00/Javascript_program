// using Array.fill method
let myarray = new Array(10);
myarray.fill(5);
console.log(myarray);
//using for loop method
let mysecondarray = new Array(10);
for (let i = 0; i < mysecondarray.length; i++) {
  mysecondarray[i] = "Hello";
}
console.log(mysecondarray);
//Array.push method
let number_array = [];
for (let i = 0; i < 10; i++) {
  number_array.push(23);
}
console.log(number_array);
